"""VideoMark 官方源码的 SSTW 评测 wrapper。

VideoMark 是生成过程中修改初始噪声的训练无关视频水印。正式比较必须先由
`external_baseline.videomark_official_runtime` 使用官方 PRC、生成 pipeline、
DDIM inversion 和 Temporal Matching Module 生成项目内 official bundle。
缺少该 bundle 时必须失败, 不能直接检测 SSTW / Wan 视频。
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

from external_baseline.official_eval_adapters.common import (
    raise_missing_official_artifacts,
    read_official_result_bundle_if_available,
    run_adapter_main,
)


BASELINE_ID = "videomark"
REQUIRED_SOURCE_FILES = (
    "embedding_and_extraction.py",
    "temporal_tamper.py",
    "src/prc.py",
    "src/pseudogaussians.py",
    "keys/64_64_512bit.pkl",
)


def _run_default(
    args: argparse.Namespace,
    source_dir: Path,
    output_json_path: Path,
) -> dict[str, Any]:
    """读取项目 workflow 生成的 VideoMark official bundle。"""

    bundled = read_official_result_bundle_if_available(
        baseline_id=BASELINE_ID,
        args=args,
        source_dir=source_dir,
        output_json_path=output_json_path,
    )
    if bundled is not None:
        return bundled
    raise_missing_official_artifacts(
        BASELINE_ID,
        (
            "missing project-owned VideoMark official bundle generated by "
            "external_baseline.videomark_official_runtime or "
            "SSTW_VIDEOMARK_NATIVE_EVAL_COMMAND"
        ),
    )


def main() -> None:
    """CLI 入口。"""

    run_adapter_main(
        baseline_id=BASELINE_ID,
        description="VideoMark 官方评测产物 wrapper。",
        required_source_files=REQUIRED_SOURCE_FILES,
        default_runner=_run_default,
    )


if __name__ == "__main__":
    main()
