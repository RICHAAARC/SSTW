"""VideoShield 官方源码的 SSTW 评测 wrapper。

VideoShield 的官方脚本以生成时 latent watermark、反演和 `wm_info.bin` 为中心。
正式运行由 `external_baseline.videoshield_official_runtime` 在 baseline 专用
Notebook 中生成项目内 official bundle。该 wrapper 仍保持轻入口职责: 优先读取
项目内 official bundle, 或在用户显式配置 `SSTW_VIDEOSHIELD_NATIVE_EVAL_COMMAND`
时调用原生命令。缺少这些项目内产物时必须失败。
"""

from __future__ import annotations

import argparse
import os
from pathlib import Path
from typing import Any

from external_baseline.official_eval_adapters.common import (
    raise_missing_official_artifacts,
    read_official_result_bundle_if_available,
    run_adapter_main,
)


BASELINE_ID = "videoshield"
REQUIRED_SOURCE_FILES = (
    "watermark.py",
    "watermark_embedding_and_extraction.py",
    "temporal_tamper_localization.py",
)


def _run_default(args: argparse.Namespace, source_dir: Path, output_json_path: Path) -> dict[str, Any]:
    """读取 VideoShield 官方评测产物 JSON。

    该分支不在单条 adapter 调用中即时执行 latent 反演, 因为那会把重型生成逻辑
    塞进统一 scoring runner。项目特定写法是先由 baseline 专用 Notebook 调用
    `external_baseline.videoshield_official_runtime` 生成 official bundle, 然后这里
    只负责读取和校验该 bundle。
    """
    bundled = read_official_result_bundle_if_available(
        baseline_id=BASELINE_ID,
        args=args,
        source_dir=source_dir,
        output_json_path=output_json_path,
    )
    if bundled is not None:
        return bundled
    raise_missing_official_artifacts(
        BASELINE_ID,
        (
            "missing project-owned VideoShield official bundle generated by "
            "external_baseline.videoshield_official_runtime or SSTW_VIDEOSHIELD_NATIVE_EVAL_COMMAND"
        ),
    )


def main() -> None:
    """CLI 入口。"""
    run_adapter_main(
        baseline_id=BASELINE_ID,
        description="VideoShield 官方评测产物 wrapper。",
        required_source_files=REQUIRED_SOURCE_FILES,
        default_runner=_run_default,
    )


if __name__ == "__main__":
    main()
