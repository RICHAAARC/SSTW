"""REVMark 官方源码的 SSTW 评测 wrapper。

REVMark 是 post-hoc 原生视频水印方法。正式公平比较必须先由
`external_baseline.revmark_official_runtime` 调用官方 Encoder / Decoder 生成
项目内 official bundle, 然后本 wrapper 读取该 bundle 并交给统一 bridge 转写
`measured_formal` records。缺少 bundle 或用户显式 native command 时必须失败。
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

from external_baseline.official_eval_adapters.common import (
    raise_missing_official_artifacts,
    read_official_result_bundle_if_available,
    run_adapter_main,
)


BASELINE_ID = "revmark"
REQUIRED_SOURCE_FILES = (
    "REVMark.py",
    "test.py",
    "checkpoints/Encoder.pth",
    "checkpoints/Decoder.pth",
)


def _run_default(args: argparse.Namespace, source_dir: Path, output_json_path: Path) -> dict[str, Any]:
    """读取 REVMark official bundle, 不在单条评分阶段重新嵌入水印。"""

    bundled = read_official_result_bundle_if_available(
        baseline_id=BASELINE_ID,
        args=args,
        source_dir=source_dir,
        output_json_path=output_json_path,
    )
    if bundled is not None:
        return bundled
    raise_missing_official_artifacts(
        BASELINE_ID,
        (
            "missing project-owned REVMark official bundle generated by "
            "external_baseline.revmark_official_runtime or SSTW_REVMARK_NATIVE_EVAL_COMMAND"
        ),
    )


def main() -> None:
    """CLI 入口。"""

    run_adapter_main(
        baseline_id=BASELINE_ID,
        description="REVMark 官方评测产物 wrapper。",
        required_source_files=REQUIRED_SOURCE_FILES,
        default_runner=_run_default,
    )


if __name__ == "__main__":
    main()
